<h3>News</h3>
<p><b>Ongoing work</b> <span class="date">02.11.2006</span></p>
<p>We are developing a new implementation of the <a href="http://www.beepcore.org/">BEEP</a> protocol due to the error-prone open-source implementation of the same protocol currently used by ACE. Further, we are working on a different network layer solution based on Java RMI/Jini due to the unstable Bonjour technology Java implementation that causes ACE crashes on some operating systems. <br> ACE milestone 5 will be released in second quarter 2007.</p>

<p><b>Bonjour related crashes</b> <span class="date">10.04.2006</span></p>
<p>Some users have reported crashes caused by Bonjour. We have identified the problem and will fix it for milestone 5.</p>

<p><b>ACE Forum</b> <span class="date">25.03.2006</span></p>
<p>Please post your experiences with ACE (enjoyable or disappointing) to the <a href="http://ace.iserver.ch/forum/">ACE forum</a>. Also if you encounter problems using ACE, let us know. Either way, it will help us to improve it.</p>

<p><b>Milestone 4 released</b> <span class="date">07.03.2006</span></p>
<p>It's our pleasure to announce the release of ACE Milestone 4. It includes bug fixes as well as some extensions at the GUI. <a href="http://ace.iserver.ch/index.php?page=8">Download now.</a></p>

<p><b>Mailing Lists</b> <span class="date">23.01.2006</span></p>
