<h3>Trac</h3>
<p>In the first stage of the project we used <a href="http://ace.iserver.ch/cgi-bin/trac.cgi">Trac</a>, a combined wiki, issue tracker, and repository
browser. Trac is still running, but we do not use it anymore. Nevertheless, it contains a link collection of all research papers we have considered for ACE. 
	To find out more about Trac, visit the <a href="http://trac.edgewall.com/" target="_blank">Trac Product Website</a>.</p>

<h3>Jira</h3>
<p>We currently work with <a href="http://ace.iserver.ch/jira">Jira</a> as our
issue tracker. This issue tracker is used (internally) to keep track of open tasks, bugs, and enhancements. To find out more about Jira, visit the <a href="http://www.atlassian.com/software/jira/" target="_blank">Jira Product Website.</a>.</p>

<h3>Confluence</h3>
<p>We use <a href="http://ace.iserver.ch/confluence">Confluence</a> as our wiki. To find out more about Confluence, visit the <a href="http://www.atlassian.com/software/confluence/" target="_blank">Confluence Product Website</a>.</p>