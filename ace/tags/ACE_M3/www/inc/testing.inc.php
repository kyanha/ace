<h2>Tests</h2>

<p>Herzlichen Dank, dass du dich bereit erkl&auml;rt hast, uns als Tester zur Seite zu stehen. An dieser Stelle nun kurz einige Hinweise, wie getestet werden soll.</p>
<p>
Wie du wohl bereits weisst, handelt es sich bei ACE um einen Text Editor. Das heisst, es k&ouml;nnen unformatierte Texte bearbeitet werden.
Am Anfang wird die Anwendung noch ziemlich wenig k&ouml;nnen. Dies wird sich mit fortlaufender Zeit &auml;ndern. 
Wir f&uuml;hren ein Dokument, das die &Auml;nderungen seit der letzten Version 
aufzeigt (release notes).
Beim Testen geht es jetzt prim&auml;r darum, die Anwendung zu verwenden, das heisst ein Dokument zu schreiben, 
dieses zu speichern, wieder zu &ouml;ffnen, Textauschnitte zu kopieren, wieder einzuf&uuml;gen... 
Der Fantasie sind hier keine Grenzen gesetzt. Wo auch immer ein Knopf oder ein Men&uuml; ist kann und soll erkundet werden, was damit bewirkt wird. Erkunden ist hier das wichtige Wort.
Es kann sein, dass gewisse Kn&ouml;pfe nichts bewirken, Fehlermeldungen sind jedoch nicht <i>normal</i>.
</p>
<p>
Solltet ihr auf Fehler stossen, oder Dinge die offensichtlich nicht funktionieren
(pr&uuml;ft auch die Release Notes, gewisse Dinge funktionieren in fr&uuml;hen Versionen nicht), so teilt uns dies mit einem e-mail an <a href="mailto:dev@ace.iserver.ch">dev@ace.iserver.ch</a> mit.
In der Mitteilung sollten insbesondere stehen:
<ul>
 <li>kurze Beschreibung, was ihr gemacht habt, bevor der Fehler auftratt (damit wir den Fehler nachvollziehen k&ouml;nnen)</li>
 <li>Auszug aus dem Logfile</li>
 <li>Version der Anwendung (siehe About Box)</li>
</ul>
</p>

