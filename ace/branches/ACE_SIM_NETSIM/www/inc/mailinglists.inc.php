<h2>ACE Mailing Lists</h2>

The ACE project has two mailing lists: the <em>ace-announce</em> and the <em>ace-devel</em> mailing lists.</p>

<h3>User List <em>ace-announce</em></h3>
<p>This is a read only low traffic mailing list for announcements and new releases.</p>
<a href="http://lists.sourceforge.net/lists/listinfo/ace-announce">Subscribe/Unsubscribe/Preferences</a>

<h3>Developer List <em>ace-devel</em></h3>
<p>The developer list is used by developers of ACE to discuss the design and implementation of ACE.</p>
<a href="http://lists.sourceforge.net/lists/listinfo/ace-devel">Subscribe/Unsubscribe/Preferences</a>